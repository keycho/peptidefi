-- 0006_prediction_markets.sql
-- LMSR/CPMM-style prediction markets keyed off peptide or supplier events.
--
-- A market may reference a peptide, a supplier, or both — at least one must be
-- non-null (enforced via CHECK). resolution_criteria is a structured jsonb
-- payload that the resolver worker reads to decide YES/NO programmatically
-- (tier1_auto) or queues for manual adjudication (tier3_manual).
--
-- Phase A leaves these tables empty. Markets are created by admins in Phase C.

-- ─── prediction_markets ─────────────────────────────────────────────────────
-- yes_pool * no_pool = liquidity_invariant (constant-product). Probability is
-- yes_pool / (yes_pool + no_pool) — kept denormalized in
-- current_yes_probability for fast frontend reads, refreshed on every trade.
create table public.prediction_markets (
  id                          bigint generated by default as identity primary key,
  peptide_id                  bigint references public.peptides(id) on delete restrict,
  supplier_id                 bigint references public.suppliers(id) on delete restrict,
  market_type                 public.prediction_market_type not null,
  resolution_tier             public.resolution_tier not null,
  question                    text not null,
  resolution_criteria         jsonb not null,
  state                       public.prediction_market_state not null default 'pending',
  yes_pool                    numeric(40, 18) not null,
  no_pool                     numeric(40, 18) not null,
  liquidity_invariant         numeric(40, 18) not null,
  current_yes_probability     numeric(8, 6),
  opened_at                   timestamptz,
  closes_at                   timestamptz not null,
  resolved_at                 timestamptz,
  outcome                     text,
  created_by                  uuid references public.users(id) on delete set null,
  created_at                  timestamptz not null default now(),
  updated_at                  timestamptz not null default now(),
  constraint prediction_markets_subject_required
    check (peptide_id is not null or supplier_id is not null),
  constraint prediction_markets_pools_nonneg
    check (yes_pool >= 0 and no_pool >= 0),
  constraint prediction_markets_invariant_nonneg
    check (liquidity_invariant >= 0),
  constraint prediction_markets_probability_range
    check (current_yes_probability is null
           or (current_yes_probability >= 0 and current_yes_probability <= 1)),
  constraint prediction_markets_outcome_when_resolved
    check ((state <> 'resolved') or (outcome is not null and resolved_at is not null))
);

create trigger prediction_markets_set_updated_at
  before update on public.prediction_markets
  for each row execute function public.set_updated_at();

create index prediction_markets_state_closes_idx
  on public.prediction_markets (state, closes_at);

create index prediction_markets_peptide_idx
  on public.prediction_markets (peptide_id)
  where peptide_id is not null;

create index prediction_markets_supplier_idx
  on public.prediction_markets (supplier_id)
  where supplier_id is not null;

-- ─── prediction_positions ───────────────────────────────────────────────────
-- One row per (user, market). Holds net yes/no shares. realized_pnl
-- accumulates as the user closes (sells) shares pre-resolution; final payout
-- on resolution is recorded as a separate point_ledger entry, not here.
create table public.prediction_positions (
  user_id          uuid not null references public.users(id) on delete restrict,
  market_id        bigint not null references public.prediction_markets(id) on delete restrict,
  yes_shares       numeric(40, 18) not null default 0,
  no_shares        numeric(40, 18) not null default 0,
  points_invested  numeric(40, 18) not null default 0,
  realized_pnl     numeric(40, 18) not null default 0,
  updated_at       timestamptz not null default now(),
  primary key (user_id, market_id),
  constraint prediction_positions_shares_nonneg
    check (yes_shares >= 0 and no_shares >= 0),
  constraint prediction_positions_invested_nonneg
    check (points_invested >= 0)
);

create trigger prediction_positions_set_updated_at
  before update on public.prediction_positions
  for each row execute function public.set_updated_at();

-- ─── prediction_trades ──────────────────────────────────────────────────────
-- Append-only trade log. side = which token, action = buy/sell. shares is
-- always positive; points_amount is points-in for buy and points-out for sell.
-- implied_probability is the marginal price after the trade.
create table public.prediction_trades (
  id                    bigint generated by default as identity primary key,
  market_id             bigint not null references public.prediction_markets(id) on delete restrict,
  user_id               uuid not null references public.users(id) on delete restrict,
  side                  text not null,
  action                text not null,
  shares                numeric(40, 18) not null,
  points_amount         numeric(40, 18) not null,
  implied_probability   numeric(8, 6) not null,
  created_at            timestamptz not null default now(),
  constraint prediction_trades_side_check check (side in ('yes', 'no')),
  constraint prediction_trades_action_check check (action in ('buy', 'sell')),
  constraint prediction_trades_shares_positive check (shares > 0),
  constraint prediction_trades_points_positive check (points_amount > 0),
  constraint prediction_trades_probability_range
    check (implied_probability >= 0 and implied_probability <= 1)
);

create index prediction_trades_market_created_idx
  on public.prediction_trades (market_id, created_at desc);

create index prediction_trades_user_created_idx
  on public.prediction_trades (user_id, created_at desc);
